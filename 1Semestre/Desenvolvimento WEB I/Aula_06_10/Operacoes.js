function Somar(valor1, valor2){
  var somar = parseInt(valor1) + parseInt(valor2)
  alert("Resultado:\n"+somar)
}

function Subtrair(valor1, valor2){
  var subtrair = parseInt(valor1) - parseInt(valor2)
  alert("Resultado:\n"+subtrair)
}

function Multiplicar(valor1, valor2){
  var multiplicar = parseInt(valor1) * parseInt(valor2)
  alert("Resultado:\n"+multiplicar)
}

function Dividir(valor1, valor2){
  var dividir = parseInt(valor1) / parseInt(valor2)
  alert("Resultado:\n"+dividir)
}